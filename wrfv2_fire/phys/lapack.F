*   from netlib@nac.no , send the message `send index from maspar'         *
*                                                                          *
*   REVISIONS:                                                             *
*                                                                          *
****************************************************************************
      implicit none
*
*     copies a vector, x, to a vector, y.
*     uses unrolled loops for increments equal to 1.
*     jack dongarra, linpack, 3/11/78.
*
*     .. Scalar Arguments ..
      INTEGER           INCX, INCY, N
*     ..
*     .. Array Arguments ..
      REAL, array(:) :: DX, DY
*     ..
*     .. Local Scalars ..
      INTEGER           m,ix,iy,jx,jy
*     ..
cmpf mpl mpl_scopy
cmpf ondpu dx,dy
*     .. Executable Statements ..
*
      IF( N.LE.0 ) return
*
*        code for both increments equal to 1
*
*
      IF( INCX.EQ.1 .AND. INCY.EQ.1 ) then
cpb        dy(1:n) = dx(1:n)
        call mpl_scopy(n,dx,dy)
      else
*
*        code for unequal increments or equal increments
*          not equal to 1
*
        m  = n - 1
        ix = 1
        iy = 1
        jx = 1 + m * incx
        jy = 1 + m * incy
*
        if ( incx .lt. 0 ) then
        ix = 1 - m * incx
        jx = 1
        endif
*
        if ( incy .lt. 0 ) then
        iy = 1 - m * incy
        jy = 1
        endif
*
        if ( incx .eq. 0) then
	   dy(iy:jy:incy) = dx(1)
           RETURN
        end if
	if (incy .eq. 0) then
	   dy(1) = dx(jx)
	   RETURN
        end if
cpb        dy(iy:jy:incy) = dx(ix:jx:incx)
        call mpl_scopy(n,dx(ix:jx:incx),dy(iy:jy:incy))
      endif
*
      RETURN
*
*     end of SCOPY .
*
       END
